Template.hello.onCreated(function helloOnCreated() {
    // counter starts at 0
    this.counter = new ReactiveVar(0);
    Template.instance().remoteUploader = new remoteUploader("http://localhost:3000","Images","modem323") ;
});

Template.hello.helpers({
    counter() {
        return Template.instance().counter.get();
    },
    uploadedFiles: function () {
        return Template.instance().remoteUploader.collection.find({'meta.appid':'modem323'});
    }
});

